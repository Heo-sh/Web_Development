package vo;

public class CigaVO {
	private int cinum, cost;
	private String name;
	
	public int getCinum() {
		return cinum;
	}
	public void setCinum(int cinum) {
		this.cinum = cinum;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
